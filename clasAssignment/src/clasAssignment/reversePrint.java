package clasAssignment;

public class reversePrint {

	public static void main(String[] args) {
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
			// 123456789 //
		}                
		for (int j = 10;  j > 1; j--) {
			System.out.println(j);
			// 987654321 //
		}
		int a=60;
		
		int b=50;
		for (int i = a; i > b; i--) {
			System.out.println(i);
		}

	}

}
